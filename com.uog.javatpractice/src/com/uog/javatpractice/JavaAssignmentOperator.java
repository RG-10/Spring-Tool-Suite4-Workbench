package com.uog.javatpractice;
                                           // Here we understand Java Assignment Operators.
                                           // It is used to assign a value.
public class JavaAssignmentOperator {

	public static void main(String[] args) {
		int Usama = 100;  
		int Raheem = 200;
		
		Usama+=15;                          //a=a+15 (a=100+15)  
		Raheem-=25;                         //b=b-25 (b=200-25)  
		
		System.out.println(Usama);  
		System.out.println(Raheem);  
		

	}

}
