package com.uog.javatpractice;
                         // JAVA Ternary Operator is a replica of If-else statement in java and works with 3 operands.

public class JavaTernaryOperator {

	public static void main(String[] args) {
		int Usama = 12;  
		int Raheem = 20;  
		int Akhter = (Usama<Raheem)?Usama:Raheem;  
		System.out.println(Akhter);  
		

	}

}                                       // Another program to understand ternary operators.
