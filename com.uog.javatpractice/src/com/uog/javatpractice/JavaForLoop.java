package com.uog.javatpractice;

public class JavaForLoop {

	public static void main(String[] args) {
		for(int Usama =  10; Usama <= 100; Usama++) {
			System.out.println("SALAM I'm Usama Raheem. A Proud Doc NAUMAN Riaz Student");
		}
			                    //This program now Print the Above Line for 100 times :)
	}

}





                                // We can also Use JAVA NESTED FOR LOOP
                                // The Example is Here below :)
     class JavaNestedforLoop {  
            public static void main(String[] args) {  
                                //loop of Usama 
            	
             for(int Usama = 1; Usama <= 3; Usama++){  
            	 
                                //loop of Raheem 
            	 
             for(int Raheem = 1; Raheem <= 3; Raheem++){ 
            	 
                  System.out.println(Usama+" "+Raheem);  
         }                      //end of Usama  
      }                         //end of Raheem
   }  
}  
     
     
