package com.uog.javatpractice;

                       //  This program is for ++ and -- understanding. 

  class JavaOperatorsPractice {
                             
	public static void main(String[] args) {
		int Usama= 100;  
		System.out.println(Usama++);     //101 (101) here the value is increased by 1. 
		System.out.println(++Usama);     //102       here again +1 in 101.  
		System.out.println(Usama--);     //102 (101) here we decrement 1 from 102. 
		System.out.println(--Usama);     //100       here again decrement 1 from 101.
		}
	}


