package com.uog.javatpractice;
                                 // This is demonstration of while Loop In JAVA.

public class JavaWhileLoop {

	public static void main(String[] args) {
		 int Usama = 10; 
		                         // This program Print values from 10 to Hundred.
		 while(Usama <= 100){
			 
		        System.out.println(Usama);  
		        Usama++;
		    }  
		}  
	}


                                 // Now we Discuss JAVA Infinitive Loop
