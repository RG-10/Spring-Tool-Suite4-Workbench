package com.uog.javatpractice;
                                    // JAVA AND OPERATOR.
public class JavaAND_Operator {

	public static void main(String[] args) {

		int  Usama = 10;  
		int  Raheem = 5;  
		int  Gondal = 20;  
		System.out.println(Usama<Raheem && Usama<Gondal);               //false && true = false  
		System.out.println(Usama<Raheem && Usama<Gondal);               //false & true = false  

	}

}
