package com.uog.javatpractice;
                       // In this Program we Learned About JAVA-Boolean OPerators.

public class JavaUnaryOperators2 {

	public static void main(String[] args) {
		int Usama = 45;
		int Raheem = 67;
		
		Boolean Gondal = true;
		Boolean Saleem = false;
		
		
		System.out.println(~Usama);   // Here the Value of Usama is -46 because it minus total+ve value starts from 0.
		System.out.println(~Raheem);  // Here the Value of Raheem is +66 beacuse it is +ve of total -ve value from 0.
		System.out.println(!Gondal);  // Here we use false operater (Opposite of BOOLEAN value)
		System.out.println(!Saleem);  // True value

	}

}
