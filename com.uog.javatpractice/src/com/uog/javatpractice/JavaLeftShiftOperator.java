package com.uog.javatpractice;

public class JavaLeftShiftOperator {

	public static void main(String[] args) {
		System.out.println(100<<2);          //100*2^2=10*4=40  
		System.out.println(100<<3);          //100*2^3=10*8=80  
		System.out.println(202<<2);          //202*2^2=20*4=80  
		System.out.println(150<<4);          //150*2^4=15*16=240
		
		
		// In this program simply JAVA LeftShift Operator is shift the bits in a value to left side of the specified num.

	}

}
