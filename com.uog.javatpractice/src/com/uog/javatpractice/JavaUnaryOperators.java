package com.uog.javatpractice;
                              // This program is for understanding the Unary Operators in JAVA.
public class JavaUnaryOperators {

	public static void main(String[] args) {
		double Usama = 1000d;
		double Raheem = 3000d;
		
		
		System.out.println(Usama++ + ++Usama);   // Here 1000+1002 Value is incremented by 2.
		System.out.println(Raheem++ + ++Raheem); // Here 3000+3001 Value is incremented by 1.
		
	}

}
