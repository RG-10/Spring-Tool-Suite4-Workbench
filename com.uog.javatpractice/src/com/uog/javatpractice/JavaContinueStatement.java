package com.uog.javatpractice;
                                  //In this we understand Java Continue Statement

public class JavaContinueStatement {

	public static void main(String[] args) {
		                         //Here we are using for loop  
	    for(int Raheem = 1; Raheem <= 10; Raheem++){  
	    	
	        if(Raheem==5){  
	        	
	                             //using continue statement
	        	
	            continue;        //it will skip the rest statement  
	        }  
	        System.out.println(Raheem);  
	                             // This program prints the values from 1 to 10 :)
	    }  
	}  
}
	  

	


