package com.uog.javatpractice;
                          // In this program we understand JAVA- Arithmetic Operators.
public class JavaArthmeticOperators {

	public static void main(String[] args) {
		int Usama = 66;
		double Raheem = 90.2d;
		
		
		System.out.println(Usama + Raheem);  //Addition 
		System.out.println(Usama - Raheem);  //Minus
		System.out.println(Usama * Raheem);  //Multiplication
		System.out.println(Usama / Raheem);  //Divide
		System.out.println(Usama % Raheem);  //Modulus
		
	}

}
