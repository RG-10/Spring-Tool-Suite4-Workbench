package com.uog.javatpractice;
                                 //Here we Understand the JAVA Break Statement.

public class JavaBreakStatment {

	public static void main(String[] args) {
		                        // Here we are Using For Loop
	    for(int Usama = 1; Usama <= 10; Usama++){  
	    	
	        if(Usama==5){  
	        	
	                            //breaking the loop  
	            break;  
	        }
	        
	        System.out.println(Usama);  
	        
	                           // In this when value becomes 5 then loop break execution.
	    }      
		
	}

}
