package com.uog.javatpractice;
                                    // This is DO-While Loop example :)
public class JavaDO_While_Loop {

	public static void main(String[] args) {
		int Raheem = 10;  
		
	    do{  
	        System.out.println(Raheem);  
	    Raheem++;  
	    
	    }while(Raheem<=100);  
	                               // This code will print values from 10 to 100.
	}  
}

                                   // Now we understand JAVA infinitive Do While Loop
                                   // Here we use true then it will make the code forever running program.
class InfinitiveDoWhileLoop{  
    public static void main(String[] args) {  
        do{ 
        	
            System.out.println("This is infinitive DO While and never stops at any cost :(");  
        }while(true);  
    }  
}  



