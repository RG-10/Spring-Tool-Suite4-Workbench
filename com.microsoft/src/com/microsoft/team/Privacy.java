package com.microsoft.team;

public class Privacy {
  
	private int PRIVACY_ID;
	
	private String PRIVACY_NAME;
	
	private String PRIVACY_DESC;

	public int getPRIVACY_ID() {
		return PRIVACY_ID;
	}

	public void setPRIVACY_ID(int pRIVACY_ID) {
		PRIVACY_ID = pRIVACY_ID;
	}

	public String getPRIVACY_NAME() {
		return PRIVACY_NAME;
	}

	public void setPRIVACY_NAME(String pRIVACY_NAME) {
		PRIVACY_NAME = pRIVACY_NAME;
	}

	public String getPRIVACY_DESC() {
		return PRIVACY_DESC;
	}

	public void setPRIVACY_DESC(String pRIVACY_DESC) {
		PRIVACY_DESC = pRIVACY_DESC;
	}
	
	
}
